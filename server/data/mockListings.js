class MockListingsProvider {
  constructor() {
    this._listings = listings;
  }

  /**
   * Search listings with optional location and filters.
   *
   * @param {{ city?: string, state?: string, zip?: string }} location
   * @param {{
   *   priceMin?: number,
   *   priceMax?: number,
   *   bedsMin?: number,
   *   bathsMin?: number,
   *   sqftMin?: number,
   *   propType?: string,
   *   features?: string[],
   *   garageRequired?: boolean,
   *   limit?: number,
   *   offset?: number,
   * }} filters
   * @returns {Promise<{ listings: object[], total: number }>}
   */
  async search(location, filters) {
    location = location || {};
    filters = filters || {};

    let results = this._listings;

    // --- Location filters ---
    if (location.city) {
      const cityLower = location.city.toLowerCase();
      results = results.filter(function (l) {
        return l.address.city.toLowerCase() === cityLower;
      });
    }
    if (location.state) {
      const stateLower = location.state.toLowerCase();
      results = results.filter(function (l) {
        return l.address.state.toLowerCase() === stateLower;
      });
    }
    if (location.zip) {
      const zip = String(location.zip);
      results = results.filter(function (l) {
        return l.address.zip === zip;
      });
    }

    // --- Price ---
    if (filters.priceMin != null) {
      results = results.filter(function (l) { return l.price >= filters.priceMin; });
    }
    if (filters.priceMax != null) {
      results = results.filter(function (l) { return l.price <= filters.priceMax; });
    }

    // --- Beds / Baths ---
    if (filters.bedsMin != null) {
      results = results.filter(function (l) { return l.beds >= filters.bedsMin; });
    }
    if (filters.bathsMin != null) {
      results = results.filter(function (l) { return l.baths >= filters.bathsMin; });
    }

    // --- Sqft ---
    if (filters.sqftMin != null) {
      results = results.filter(function (l) { return l.sqft >= filters.sqftMin; });
    }

    // --- Property type ---
    if (filters.propType && filters.propType !== 'any') {
      const typeMap = {
        house: 'single_family',
        condo: 'condo',
        townhome: 'townhome',
        multi_family: 'multi_family',
        land: 'land',
      };
      const mapped = typeMap[filters.propType] || filters.propType;
      results = results.filter(function (l) {
        return l.prop_type === mapped;
      });
    }

    // --- Garage ---
    if (filters.garageRequired) {
      results = results.filter(function (l) { return l.garage === true; });
    }

    // --- Features (all must be present) ---
    if (filters.features && filters.features.length > 0) {
      results = results.filter(function (l) {
        var featureLower = l.features.map(function (f) { return f.toLowerCase(); });
        return filters.features.every(function (f) {
          var fLower = f.toLowerCase();
          return featureLower.some(function (lf) { return lf.includes(fLower); });
        });
      });
    }

    const total = results.length;

    // --- Pagination ---
    const offset = filters.offset || 0;
    const limit = filters.limit || 20;
    results = results.slice(offset, offset + limit);

    return { listings: results, total: total };
  }

  /**
   * Get a single listing by id.
   *
   * @param {string} id
   * @returns {Promise<object|null>}
   */
  async getById(id) {
    var found = this._listings.find(function (l) { return l.id === id; });
    return found || null;
  }
}

module.exports = { MockListingsProvider, listings };
